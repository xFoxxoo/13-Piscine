#include <unistd.h>

int	main(int ac, char **av)
{
	int	i = 0;
	
	if (ac != 2)
	{
		write (1, "a", 1);
		return (0);
	}
	while (av[1][i++] && ac == 2)
	{
		if (av[1][i] == 'a')
		{
			write(1, "a", 1);
			break ;
		}
	}
	write (1, "\n", 1);
}
