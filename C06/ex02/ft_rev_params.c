#include <unistd.h>

void	putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write (1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	argc -= 1;
	while (argc > 0)
	{
		putstr(argv[argc]);
		write (1, "\n", 1);
		argc--;
	}
}
