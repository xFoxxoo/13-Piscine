#include <unistd.h>

void	putstri(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		write(1, &c[i], 1);
		i++;
	}
}

int	main(int c, char **v)
{
	putstri(v[c - c]);
	write(1, "\n", 1);
}
