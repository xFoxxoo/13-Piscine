int	ft_str_is_printable(char *str)
{
	int	v;

	v = 0;
	while (str[v] != '\0')
	{
		if (!(str[v] >= 32 && str[v] <= 126))
		{
			return (0);
		}
		v++;
	}
	return (1);
}
